/* write a program that performs  LL COMPOUND assignment operations 
on an integer*/

#include"iostream"
#include"conio.h"
using namespace std;
 main()
 {
 	int a=10;
 	cout<<" Values of a "<<a<<endl;
 	a+=5;
 	cout<<"Values of a+=5 : "<<a<<endl;
 	a-=5;
	cout<<"Values of a-=5 : "<<a<<endl;
	a*=2;
	cout<<"Values of a*=2 : "<<a<<endl;
	a/=2;
	cout<<"values of a/=2 : "<<a<<endl;
	a%=2;
	cout<<"values of a%=2 : "<<a<<endl;
	
 	getch();
 }
